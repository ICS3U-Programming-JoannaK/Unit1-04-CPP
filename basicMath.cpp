#include <cmath>
#include <iostream>

int main() {
    std::cout << "9+2= " << 9 + 2 << std::endl;
    std::cout << "10-5= " << 10 - 5 << std::endl;
    std::cout << "100*10= " << 100 * 10 << std::endl;
    std::cout << "10/144= " << float(10) / float(144) << std::endl;
    std::cout << "3**10= " << pow(3, 10) << std::endl;
    std::cout << "81**0.5= " << pow(81, 0.5) << std::endl;
}